def _build_database() -> dict:
    # Transforme les lignes compactes en dictionnaire lisible par le reste du backend.
    database = {}

    for row in _PROPELLANT_ROWS:
        (
            key, label, source, line, total_impulse_ns, isp_s,
            full_mass_kg, ejected_mass_kg, empty_mass_kg,
            length_m, diameter_m, cg_full_local_m, cg_empty_local_m,
        ) = row

        database[key] = {
            "label": label,
            "source": source,
            "line": line,
            "total_impulse_ns": total_impulse_ns,
            "isp_s": isp_s,
            "motor_full_mass_kg": full_mass_kg,
            "ejected_mass_kg": ejected_mass_kg,
            "motor_empty_mass_kg": empty_mass_kg,
            "motor_length_m": length_m,
            "motor_diameter_m": diameter_m,
            "motor_cg_full_local_m": cg_full_local_m,
            "motor_cg_empty_local_m": cg_empty_local_m,
        }

    return database


PROPELLANT_DATABASE = _build_database()


def _normalize_key(name: str | None) -> str:
    # Prépare une chaîne pour comparer deux noms de propulseurs.
    if name is None:
        return ""

    text = str(name).strip().lower().replace("µ", "micro")
    text = re.sub(r"[^a-z0-9]+", "_", text)
    text = re.sub(r"_+", "_", text)
    return text.strip("_")


def _compact_key(name: str | None) -> str:
    # Variante encore plus tolérante pour la recherche.
    return re.sub(r"[^a-z0-9]+", "", _normalize_key(name))


def get_propellant_data(motor_type: str | None) -> dict | None:
    # Retrouve un moteur par clé technique ou par libellé.
    key = _normalize_key(motor_type)
    if not key:
        return None

    if key in PROPELLANT_DATABASE:
        return PROPELLANT_DATABASE[key]

    compact = _compact_key(key)
    for data_key, data in PROPELLANT_DATABASE.items():
        if compact == _compact_key(data_key) or compact == _compact_key(data.get("label")):
            return data

    for data_key, data in PROPELLANT_DATABASE.items():
        if _compact_key(data.get("label")).startswith(compact) or _compact_key(data_key).startswith(compact):
            return data

    return None


def list_propellants() -> list[dict]:
    # Liste complète pour la sélection côté interface.
    items = []
    for key, data in PROPELLANT_DATABASE.items():
        items.append({
            "key": key,
            "label": data.get("label", key),
            "source": data.get("source"),
            "diameter_m": data.get("motor_diameter_m"),
            "length_m": data.get("motor_length_m"),
            "full_mass_kg": data.get("motor_full_mass_kg"),
            "empty_mass_kg": data.get("motor_empty_mass_kg"),
        })

    return sorted(items, key=lambda item: item["label"].lower())


def search_propellants(prefix: str | None) -> list[dict]:
    # Filtre d'autocomplétion : si la recherche est vide, on laisse défiler tout le catalogue.
    cleaned = _compact_key(prefix)
    items = list_propellants()
    if not cleaned:
        return items

    return [
        item for item in items
        if _compact_key(item.get("label")).startswith(cleaned)
        or _compact_key(item.get("key")).startswith(cleaned)
    ]